Introducción a Vectores.


Qué es un Vector? 

- Un vector es una concatenación de datos donde 
todos los datos son del mismo tipo; lo dicho, 
se posicionan de manera contigua. 

En otros lenguajes de programación al 'Vector' se le 
llama: 'Array' o, en español, 'Arreglo'.

Si usted se recuerda, todos los arreglos tienen 
enumeración (vienen enumerados), porque realmente 
son secuencias de valores; en otras palabras, es un 
set con cierto orden, el cual siempre va a tener un
inicio y un fin. Lo mismo pasa en R con sus vectores.

Entonces, el primer dato de un vector sería el inicio
del mismo y correspondería al elemento número 1. 

En otros lenguajes como C o Python los arreglos 
comienzan desde 0, no desde 1 como en R.


Vector tipo Character en R

Si dentro de la longitud de cualquier vector usted 
se encuentra con un dato entre comillas dentro de él 
(letra, número, etc), tan solo uno, 
estariamos hablando de un vector de tipo 'Character'.


Importante: Todo en R, inclusive un número o un solo
caracter, siempre serán un Vector. 


